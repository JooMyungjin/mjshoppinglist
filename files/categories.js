// ── 분류 함수 ─────────────────────────────────────────────────────────────────
function classifyItem(name) {
  if (!name) return '기타';
  const n = name.toLowerCase();
  for (const rule of CATEGORY_RULES) {
    const target = rule.matchName ? name : n;
    if (rule.keywords.some(kw => target.includes(kw))) return rule.name;
  }
  return '기타';
}

